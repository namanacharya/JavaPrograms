package assignment;
//print 1-10 using while loop

public class whileLoop 
{
	public static void main(String[] args)
	{
		int n=1;
		System.out.println("Natural numbers within 1 to 10");
		System.out.println("------------------------------");
		while(n<=10)
		{
			System.out.print(" "+n+" ");
			n++;
		}
	}
}
