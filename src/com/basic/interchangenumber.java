package com.basic;

public class interchangenumber {
	public static void main(String[] args) {
		int a,b,temp;
		a=10;
		b=20;
		temp=a;
		
		System.out.println("Value of A: " +a);
		System.out.println("Value of B: "+b);
		System.out.println("Swapping value");
		a=b;
		System.out.println("Value of A: "+a);
		b=temp;
		System.out.println("Value of B: "+b);
		
	}
	
}
